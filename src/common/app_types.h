#pragma once

#include <cstdint>

namespace mp {

// 系统总状态。
//
// 这个枚举描述的是“整台设备当前处于什么阶段”。
// 它通常会被上层状态机、日志系统、UI 指示或协议上报共同使用。
//
// 设计上把状态拆得稍微细一点，有两个好处：
// 1. 初学时更容易看懂系统启动到运行的完整流程。
// 2. 后续做日志与故障定位时，更容易知道系统卡在哪个阶段。
enum class SystemState : uint8_t {
  BOOT = 0,   // 刚上电或刚复位，系统只完成最早期启动。
  INIT,       // 正在初始化软件模块、配置参数和基础资源。
  SELF_TEST,  // 执行自检，确认基础能力是否可用。
  IDLE,       // 空闲状态，系统在线但当前没有待执行任务。
  READY,      // 已准备就绪，可以开始接收或执行打印任务。
  RUNNING,    // 正在执行活动任务，例如接收数据、走纸或打印。
  ERROR,      // 进入错误状态，需要等待处理或恢复。
  RECOVERY,   // 正在尝试从错误中恢复。
  SAFE_MODE,  // 进入安全模式，只保留最小能力，避免继续扩大风险。
  LOW_POWER,  // 低功耗或低电量受限运行状态。
  SHUTDOWN,   // 正在关机或准备停止服务。
};

// 打印任务状态。
//
// 这个枚举只关心“某一份打印任务”的生命周期，
// 不直接表示整机状态，所以它和 SystemState 是两个不同维度。
// 例如：
// - 系统可能处于 READY，而打印任务处于 NONE。
// - 也可能系统处于 RUNNING，而打印任务处于 PRINTING。
enum class PrintJobState : uint8_t {
  NONE = 0,  // 当前没有打印任务。
  RECEIVING, // 正在接收打印数据。
  QUEUED,    // 数据已收完，任务进入等待执行队列。
  PRINTING,  // 正在实际打印。
  PAUSED,    // 任务被暂停，后续可能继续。
  DONE,      // 任务正常完成。
  CANCELLED, // 任务被主动取消。
  FAILED,    // 任务执行失败，未正常完成。
};

// 传感器数据有效性。
//
// 很多传感器值并不是“有值就一定可信”，
// 所以把“值本身”和“值是否可信”分开处理会更安全。
//
// 例如：
// - 读取成功且在合理范围内时可标为 VALID。
// - 读取异常、超范围或校验失败时可标为 INVALID。
// - 很久没有刷新时可标为 STALE，提醒上层不要继续依赖旧数据。
enum class SensorValidity : uint8_t {
  VALID = 0, // 数据有效，可直接参与控制或显示。
  INVALID,   // 数据无效，不应直接使用。
  STALE,     // 数据过旧，虽然存在，但可信度不足。
};

// 日志级别。
//
// 这个枚举用于统一日志输出的重要程度，
// 便于后续按级别过滤日志，避免串口或存储里充满无关信息。
enum class LogLevel : uint8_t {
  FATAL = 0, // 致命问题，通常表示系统已无法正常继续运行。
  ERROR,     // 错误，功能已受影响，需要处理。
  WARN,      // 警告，系统还能继续，但存在风险或异常条件。
  INFO,      // 常规运行信息，适合观察主流程。
  DEBUG,     // 调试信息，帮助定位实现细节问题。
  TRACE,     // 最细粒度追踪信息，通常只在深度排查时开启。
};

}  // namespace mp
